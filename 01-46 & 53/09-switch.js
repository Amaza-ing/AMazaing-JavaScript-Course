let day = "domingo";

switch (day) {
  case "lunes":
    console.log("Es el primer día de la semana");
    break;
  case "martes":
    console.log("Es el segundo día de la semana");
    break;
  case "vienes":
    console.log("Casi es fin de semana");
    break;
  default:
    console.log("Es otro día");
    break;
}
